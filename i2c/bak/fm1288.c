/* FM1288 module frame */

static int nv_module_fm1288_init (const nv_module_config_t *config)
{
	int ret = -1;
	printk("nv_module_fm1288_init\n");

	fm1288_info.chip_func = nv_chip();
	if (!fm1288_info.chip_func) {
		nv_error("get chip function failed");
		return -1;
	}
	spin_lock_init(&fm1288_info.lock);

	spin_lock(&fm1288_info.lock);
	fm1288_info.reset_pin.group = config->i2c.reset.group;
	fm1288_info.reset_pin.number = config->i2c.reset.number;
	fm1288_info.reset_pin_value = config->i2c.reset.normal_value;

	fm1288_info.i2c_pin.scl.group = config->i2c.scl.group;
	fm1288_info.i2c_pin.scl.number = config->i2c.scl.number;
	fm1288_info.i2c_pin.sda.group = config->i2c.sda.group;
	fm1288_info.i2c_pin.sda.number = config->i2c.sda.number;

	fm1288_info.init_data.data = fm1288_init_data;
	fm1288_info.init_data.len = sizeof(fm1288_init_data);
	spin_unlock(&fm1288_info.lock);

	ret = nv_module_fm1288_gpio_init();
	if (ret) {
		nv_error("fm1288: gpio init failed, ret = %d", ret);
		return -1;
	}

	ret = fm1288_info.chip_func->i2c->nv_i2c_init(&fm1288_info.i2c_pin);
	if (ret) {
		nv_error("fm1288: i2c init failed, ret = %d", ret);
		return -1;
	}

	return 0;
}

static int nv_module_fm1288_uninit(void)
{
	printk("nv_module_fm1288_uninit\n");

	fm1288_info.chip_func = NULL;

	return 0;
}

static int nv_module_fm1288_operation(unsigned int cmd,unsigned long args)
{
	int ret = -1;
	u8 fm1288_cmd = IOC_CMD(cmd);
	fm1288_init_data_t data = {0};

	switch (fm1288_cmd) {
	case NV_MODULE_FM1288_CHIP_RESET:
		ret = nv_module_fm1288_chip_reset();
		break;
	case NV_MODULE_FM1288_INIT_REGISTER:
		ret = nv_module_fm1288_init_register();
		break;
	case NV_MODULE_FM1288_READ_DATA:
		ret = nv_module_fm1288_read_data(&data);
		break;
	case NV_MODULE_FM1288_WRITE_DATA:
		ret = nv_module_fm1288_write_data(&data);
		break;
	default:
		nv_error("cmd error no = %d \n", fm1288_cmd);
		ret = -EINVAL;
		break;
	}

	if (ret < 0) {
		nv_error("fm1288 operations failed");
	}

	return ret;
}

nv_module_ops_t nv_module_fm1288 = {
	.init = nv_module_fm1288_init,
	.uninit = nv_module_fm1288_uninit,
	.operation = nv_module_fm1288_operation,
};

